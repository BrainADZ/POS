/** fills in the new fields so seed data matches the MenuItem shape */
function item(
  m: Omit<MenuItem, 'veg' | 'variants' | 'discountPct' | 'showOnKiosk' | 'showOnPos' | 'gradient'> &
    Partial<Pick<MenuItem, 'variants' | 'discountPct' | 'showOnKiosk' | 'showOnPos' | 'gradient'>>
): MenuItem {
  return {
    gradient: TILE,
    variants: [],
    discountPct: 0,
    showOnKiosk: true,
    showOnPos: true,
    veg: m.foodType === 'veg',
    ...m,
  };
}

